import { join } from 'node:path';
import { pathExists } from '../fs/fs.mjs';
import { reflinkCopyDir } from './reflink_copy_dir.mjs';

function isTruthy(v) {
  const s = String(v ?? '').trim().toLowerCase();
  return s === '1' || s === 'true' || s === 'yes' || s === 'on';
}

export async function seedNodeModulesFromBase({
  baseDir,
  worktreeDir,
  env = process.env,
  copyDir = reflinkCopyDir,
} = {}) {
  const enabled = (() => {
    const raw = String(env.HAPPIER_STACK_WT_SEED_NODE_MODULES ?? '').trim();
    if (raw) return isTruthy(raw);
    // Default: enable in sandboxes, disable elsewhere.
    return Boolean(String(env.HAPPIER_STACK_SANDBOX_DIR ?? '').trim());
  })();

  if (!enabled) return { ok: true, seeded: false, reason: 'disabled' };

  const from = join(String(baseDir ?? ''), 'node_modules');
  const to = join(String(worktreeDir ?? ''), 'node_modules');
  if (!(await pathExists(from))) return { ok: true, seeded: false, reason: 'base-missing' };
  if (await pathExists(to)) return { ok: true, seeded: false, reason: 'dest-exists' };

  const res = await copyDir({ srcDir: from, destDir: to });
  if (!res?.ok) {
    return { ok: true, seeded: false, reason: res?.reason ?? 'copy-failed', error: res?.error ?? null };
  }
  return { ok: true, seeded: true, reason: 'seeded' };
}
