import test from 'node:test';
import assert from 'node:assert/strict';
import { mkdtemp, mkdir, rm, writeFile } from 'node:fs/promises';
import { join } from 'node:path';
import { tmpdir } from 'node:os';

import { seedNodeModulesFromBase } from './seed_node_modules.mjs';

test('seedNodeModulesFromBase is disabled by default outside sandbox', async (t) => {
  const dir = await mkdtemp(join(tmpdir(), 'hstack-seed-nm-'));
  t.after(async () => rm(dir, { recursive: true, force: true }));

  const base = join(dir, 'base');
  const wt = join(dir, 'wt');
  await mkdir(join(base, 'node_modules'), { recursive: true });
  await mkdir(wt, { recursive: true });

  let called = false;
  const res = await seedNodeModulesFromBase({
    baseDir: base,
    worktreeDir: wt,
    env: {},
    copyDir: async () => {
      called = true;
      return { ok: true };
    },
  });
  assert.equal(res.seeded, false);
  assert.equal(res.reason, 'disabled');
  assert.equal(called, false);
});

test('seedNodeModulesFromBase seeds node_modules in sandbox by default', async (t) => {
  const dir = await mkdtemp(join(tmpdir(), 'hstack-seed-nm-'));
  t.after(async () => rm(dir, { recursive: true, force: true }));

  const base = join(dir, 'base');
  const wt = join(dir, 'wt');
  await mkdir(join(base, 'node_modules'), { recursive: true });
  await writeFile(join(base, 'node_modules', 'x.txt'), 'x\n', 'utf-8');
  await mkdir(wt, { recursive: true });

  let called = false;
  const res = await seedNodeModulesFromBase({
    baseDir: base,
    worktreeDir: wt,
    env: { HAPPIER_STACK_SANDBOX_DIR: '/tmp/sandbox' },
    copyDir: async ({ srcDir, destDir }) => {
      called = true;
      assert.equal(srcDir, join(base, 'node_modules'));
      assert.equal(destDir, join(wt, 'node_modules'));
      await mkdir(destDir, { recursive: true });
      return { ok: true };
    },
  });
  assert.equal(res.seeded, true);
  assert.equal(called, true);
});

test('seedNodeModulesFromBase does nothing if dest already has node_modules', async (t) => {
  const dir = await mkdtemp(join(tmpdir(), 'hstack-seed-nm-'));
  t.after(async () => rm(dir, { recursive: true, force: true }));

  const base = join(dir, 'base');
  const wt = join(dir, 'wt');
  await mkdir(join(base, 'node_modules'), { recursive: true });
  await mkdir(join(wt, 'node_modules'), { recursive: true });

  let called = false;
  const res = await seedNodeModulesFromBase({
    baseDir: base,
    worktreeDir: wt,
    env: { HAPPIER_STACK_SANDBOX_DIR: '/tmp/sandbox' },
    copyDir: async () => {
      called = true;
      return { ok: true };
    },
  });
  assert.equal(res.seeded, false);
  assert.equal(res.reason, 'dest-exists');
  assert.equal(called, false);
});
